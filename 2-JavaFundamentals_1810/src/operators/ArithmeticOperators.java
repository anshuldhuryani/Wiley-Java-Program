package operators;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
			int num1 = 100, num2 = 200, result;
			
			result = num1 + num2;
			System.out.println(result);
			
			result = num1 - num2;
			System.out.println(result);
			
			result = num1 * num2;
			System.out.println(result);
			
			result = num1 / num2;
			System.out.println(result);
			
			result = num1 % num2;
			System.out.println(result);
	}

}
