package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int num1 = 5, num2 = 10;
		
		System.out.println(num1 == num2); 	// False
		System.out.println(num1 != num2);		// True
		System.out.println(num1>num2);			// False
		System.out.println(num1<num2);			// True
		System.out.println(num1>=num2);		// False
		System.out.println(num1<=num2);		// True

	}

}
